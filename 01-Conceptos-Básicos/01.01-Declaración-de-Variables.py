# Declaración de variables
numero = 10
Numero = 20
saludo = "Hola Mundo !!!"

# Mostrar el contenido de las variables
print(numero)
print(Numero)           # diferencía entre mayúsculas y minúsculas
print(saludo)

print(numero + Numero)
print(numero - 25)
print("Saludo: " + saludo)

print(36)
print("Saludos")
print("")

# Mostrar el tipo de las variables
print(type(numero))
print(type(saludo))

# Mostar el tipo de diferentes valores utilizando la función TYPE
print(type(3))
print(type(3.1))
print(type("3"))
print(type("tres"))
print(type(3==3))
print(type(('1', '2', '3')))
print(type(["1", "2", "3"]))
print(type({"1", "2", "3"}))
print(type([1, 2, 3]))